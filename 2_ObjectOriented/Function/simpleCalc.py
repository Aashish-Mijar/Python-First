def add(a, b):
    return a+b

def subt(a, b):
    return a-b

def mul(a, b):
    return a*b

def div(a, b):
    return a/b

addResult=add(2,3)
print("The result of addition is:", addResult)

subtResult=subt(9,3)
print("The result of subtraction is:", subtResult)

mulResult=mul(5, 4)
print("The result of multiplication is:", mulResult)

divResult=div(7, 3)
print("The result of division is:", divResult)